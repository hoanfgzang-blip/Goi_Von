-- 8. SP Báo cáo Mặt hàng Bán chạy
CREATE PROCEDURE SP_MatHangBanChay
    @TuNgay DATE, @DenNgay DATE, @TopN INT = 10, @ID_NHACC NVARCHAR(10) = NULL, @NoiSX NVARCHAR(100) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SELECT TOP (@TopN) sp.ID_SANPHAM, sp.TEN_SANPHAM, sp.LOAI_HANG, ncc.TEN_NHACC, sp.NOI_SX,
        SUM(ct.SO_LUONGBAN) AS TongSoLuongBan
    FROM DON_HANG AS ct
    JOIN HOADON AS hd ON ct.ID_HOADON = hd.ID_HOADON
    JOIN SAN_PHAM AS sp ON ct.ID_SANPHAM = sp.ID_SANPHAM
    JOIN NHACC AS ncc ON sp.ID_NHACC = ncc.ID_NHACC
    WHERE hd.TRANG_THAI = 3 AND hd.NGAY_TAO BETWEEN @TuNgay AND @DenNgay
        AND (@ID_NHACC IS NULL OR sp.ID_NHACC = @ID_NHACC)
        AND (@NoiSX IS NULL OR sp.NOI_SX = @NoiSX)
    GROUP BY sp.ID_SANPHAM, sp.TEN_SANPHAM, sp.LOAI_HANG, ncc.TEN_NHACC, sp.NOI_SX
    ORDER BY TongSoLuongBan DESC;
END;
GO

-- 9. SP Tìm Khách Hàng Theo Loại Hàng
CREATE PROCEDURE SP_TimKhachHangTheoLoaiHang
    @LoaiHang NVARCHAR(50), @TuNgay DATE, @DenNgay DATE
AS
BEGIN
    SET NOCOUNT ON;
    SELECT DISTINCT kh.ID_KH, kh.TEN_KH, kh.SO_DTH, kh.DIA_CHI
    FROM KHACHHANG AS kh JOIN HOADON AS hd ON kh.ID_KH = hd.ID_KH
    JOIN DON_HANG AS ct ON hd.ID_HOADON = ct.ID_HOADON
    JOIN SAN_PHAM AS sp ON ct.ID_SANPHAM = sp.ID_SANPHAM
    WHERE hd.TRANG_THAI = 3 AND sp.LOAI_HANG = @LoaiHang AND hd.NGAY_TAO BETWEEN @TuNgay AND @DenNgay;
END;
GO

-- 10. SP Tìm Khách Hàng Theo Thời Gian
CREATE PROCEDURE SP_TimKhachHangTheoThoiGian
    @TuNgay DATE, @DenNgay DATE
AS
BEGIN
    SET NOCOUNT ON;
    SELECT DISTINCT kh.ID_KH, kh.TEN_KH, kh.SO_DTH, kh.DIA_CHI
    FROM KHACHHANG AS kh JOIN HOADON AS hd ON kh.ID_KH = hd.ID_KH
    WHERE hd.TRANG_THAI = 3 AND hd.NGAY_TAO BETWEEN @TuNgay AND @DenNgay;
END;
GO

-- 11. SP Báo cáo Mặt hàng Không bán được
CREATE PROCEDURE SP_MatHangKhongBanDuoc
    @TuNgay DATE, @DenNgay DATE, @ID_NHACC NVARCHAR(10) = NULL, @NoiSX NVARCHAR(100) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SELECT sp.ID_SANPHAM, sp.TEN_SANPHAM, sp.LOAI_HANG, ncc.TEN_NHACC, sp.NOI_SX, kh.SO_LUONG AS SoLuongTonKho
    FROM SAN_PHAM AS sp JOIN KHO_HANG AS kh ON sp.ID_SANPHAM = kh.ID_SANPHAM
    JOIN NHACC AS ncc ON sp.ID_NHACC = ncc.ID_NHACC
    WHERE sp.TRANG_THAI_KD = 1 AND (@ID_NHACC IS NULL OR sp.ID_NHACC = @ID_NHACC)
        AND (@NoiSX IS NULL OR sp.NOI_SX = @NoiSX)
        AND NOT EXISTS (
            SELECT 1 FROM DON_HANG ct JOIN HOADON hd ON ct.ID_HOADON = hd.ID_HOADON
            WHERE ct.ID_SANPHAM = sp.ID_SANPHAM AND hd.TRANG_THAI = 3 AND hd.NGAY_TAO BETWEEN @TuNgay AND @DenNgay
        )
    ORDER BY ncc.TEN_NHACC, sp.TEN_SANPHAM;
END;
GO

-- 12. SP In Hóa Đơn
CREATE PROCEDURE SP_InHoaDon
    @ID_HOADON NVARCHAR(10)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT hd.ID_HOADON, hd.NGAY_TAO, hd.CHIETKHAU, hd.TONG_TIEN_SP, hd.TONG_THANH_TOAN,
        hd.HINH_THUC, hd.TRANG_THAI, hd.NGAY_GIAO_DU_KIEN, hd.NGAY_GIAO_THUC_TE,
        kh.TEN_KH, kh.SO_DTH AS DTH_KH, kh.DIA_CHI AS DiaChi_KH, s.TEN_HANG_SHIPPER
    FROM HOADON hd JOIN KHACHHANG kh ON hd.ID_KH = kh.ID_KH
    LEFT JOIN SHIPPER s ON hd.ID_SHIPPER = s.ID_SHIPPER
    WHERE hd.ID_HOADON = @ID_HOADON;

    SELECT ct.ID_SANPHAM, sp.TEN_SANPHAM, ct.SO_LUONGBAN, ct.DON_GIA,
        (ct.SO_LUONGBAN * ct.DON_GIA) AS Thanh_Tien
    FROM DON_HANG ct JOIN SAN_PHAM sp ON ct.ID_SANPHAM = sp.ID_SANPHAM
    WHERE ct.ID_HOADON = @ID_HOADON;
END;
GO










-- 1. Trigger Tích Điểm Tích Lũy
CREATE TRIGGER TG_CongDiemTichLuy
ON HOADON
AFTER UPDATE
AS
BEGIN
    IF NOT UPDATE(TRANG_THAI) RETURN;

    DECLARE @ID_KH NVARCHAR(10), @ID_HOADON NVARCHAR(10);
    DECLARE @TongTien MONEY, @TrangThaiMoi INT, @TrangThaiCu INT, @DiemThuong INT;

    SELECT @ID_KH = i.ID_KH, @ID_HOADON = i.ID_HOADON, @TongTien = i.TONG_THANH_TOAN,
           @TrangThaiMoi = i.TRANG_THAI, @TrangThaiCu = d.TRANG_THAI
    FROM inserted i JOIN deleted d ON i.ID_HOADON = d.ID_HOADON;

    IF (@TrangThaiMoi = 3 AND @TrangThaiCu <> 3 AND @TongTien > 0)
    BEGIN
        SET @DiemThuong = CAST(@TongTien / 10000 AS INT);
        IF (@DiemThuong > 0)
        BEGIN
            UPDATE KHACHHANG SET DIEM_TICH_LUY = DIEM_TICH_LUY + @DiemThuong WHERE ID_KH = @ID_KH;
            INSERT INTO LICH_SU_DIEM (ID_KH, ID_HOADON, SO_DIEM_THAY_DOI, LY_DO)
            VALUES (@ID_KH, @ID_HOADON, @DiemThuong, N'Tích điểm HĐ ' + @ID_HOADON);
        END
    END
END;
GO

-- 2. Trigger Cập nhật Ngày Giao Dự kiến và Thực tế
CREATE TRIGGER TG_CapNhatNgayGiao_Online
ON HOADON
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    IF NOT UPDATE(TRANG_THAI) RETURN;

    -- Cập nhật NGÀY GIAO DỰ KIẾN (Trạng thái 1 -> 2)
    UPDATE hd
    SET hd.NGAY_GIAO_DU_KIEN = DATEADD(day, 3, GETDATE()),
        hd.ID_SHIPPER = ISNULL(hd.ID_SHIPPER, (SELECT TOP 1 ID_SHIPPER FROM SHIPPER))
    FROM HOADON hd JOIN inserted i ON hd.ID_HOADON = i.ID_HOADON JOIN deleted d ON hd.ID_HOADON = d.ID_HOADON
    WHERE i.TRANG_THAI = 2 AND d.TRANG_THAI = 1 AND i.HINH_THUC = 2;

    -- Cập nhật NGÀY GIAO THỰC TẾ (Trạng thái -> 3)
    UPDATE hd
    SET hd.NGAY_GIAO_THUC_TE = GETDATE()
    FROM HOADON hd JOIN inserted i ON hd.ID_HOADON = i.ID_HOADON JOIN deleted d ON hd.ID_HOADON = d.ID_HOADON
    WHERE i.TRANG_THAI = 3 AND d.TRANG_THAI <> 3 AND i.HINH_THUC = 2;
END;
GO
