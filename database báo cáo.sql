-- 1. SP Thêm Sản Phẩm
CREATE PROCEDURE SP_ThemSanPham
    @ID_SANPHAM NVARCHAR(10), @TEN_SANPHAM NVARCHAR(100), @ID_NHACC NVARCHAR(10),
    @DON_VI NVARCHAR(50), @GIA_BAN MONEY, @NGAY_SX DATE = NULL,
    @LOAI_HANG NVARCHAR(50) = NULL, @NOI_SX NVARCHAR(100) = NULL
AS
BEGIN
    SET NOCOUNT ON; BEGIN TRANSACTION;
    BEGIN TRY
        INSERT INTO SAN_PHAM (ID_SANPHAM, TEN_SANPHAM, ID_NHACC, DON_VI, GIA_BAN, NGAY_SX, LOAI_HANG, NOI_SX)
        VALUES (@ID_SANPHAM, @TEN_SANPHAM, @ID_NHACC, @DON_VI, @GIA_BAN, @NGAY_SX, @LOAI_HANG, @NOI_SX);
        INSERT INTO KHO_HANG (ID_SANPHAM, SO_LUONG) VALUES (@ID_SANPHAM, 0);
        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION; THROW;
    END CATCH
END;
GO

-- 2. SP Sửa Sản Phẩm
CREATE PROCEDURE SP_SuaSanPham
    @ID_SANPHAM NVARCHAR(10), @TEN_SANPHAM_MOI NVARCHAR(100) = NULL, @ID_NHACC_MOI NVARCHAR(10) = NULL,
    @DON_VI_MOI NVARCHAR(50) = NULL, @GIA_BAN_MOI MONEY = NULL, @NGAY_SX_MOI DATE = NULL,
    @LOAI_HANG_MOI NVARCHAR(50) = NULL, @NOI_SX_MOI NVARCHAR(100) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    UPDATE SAN_PHAM
    SET  TEN_SANPHAM = ISNULL(@TEN_SANPHAM_MOI, TEN_SANPHAM), ID_NHACC = ISNULL(@ID_NHACC_MOI, ID_NHACC),
        DON_VI = ISNULL(@DON_VI_MOI, DON_VI), GIA_BAN = ISNULL(@GIA_BAN_MOI, GIA_BAN),
        NGAY_SX = ISNULL(@NGAY_SX_MOI, NGAY_SX), LOAI_HANG = ISNULL(@LOAI_HANG_MOI, LOAI_HANG),
        NOI_SX = ISNULL(@NOI_SX_MOI, NOI_SX)
    WHERE ID_SANPHAM = @ID_SANPHAM;
END;
GO

-- 3. SP Xóa Sản Phẩm (Xóa mềm)
CREATE PROCEDURE SP_XoaSanPham @ID_SANPHAM NVARCHAR(10)
AS
BEGIN
    UPDATE SAN_PHAM SET TRANG_THAI_KD = 0 WHERE ID_SANPHAM = @ID_SANPHAM;
    UPDATE KHO_HANG SET SO_LUONG = 0 WHERE ID_SANPHAM = @ID_SANPHAM;
END;
GO

-- 4. SP Nhập Kho
CREATE PROCEDURE SP_NhapKho
    @ID_PHIEUNHAP NVARCHAR(10), @ID_SANPHAM NVARCHAR(10), @ID_NHACC NVARCHAR(10),
    @SO_LUONGNHAP INT, @GIA_NHAP MONEY
AS
BEGIN
    SET NOCOUNT ON; BEGIN TRANSACTION;
    BEGIN TRY
        INSERT INTO NHAP_HANG (ID_PHIEUNHAP, ID_NHACC, ID_SANPHAM, SO_LUONGNHAP, GIA_NHAP, NGAY_NHAP)
        VALUES (@ID_PHIEUNHAP, @ID_NHACC, @ID_SANPHAM, @SO_LUONGNHAP, @GIA_NHAP, GETDATE());
        UPDATE KHO_HANG SET SO_LUONG = SO_LUONG + @SO_LUONGNHAP WHERE ID_SANPHAM = @ID_SANPHAM;
        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION; THROW;
    END CATCH
END;
GO

-- 5. SP Tạo Hóa Đơn
CREATE PROCEDURE SP_TaoHoaDon
    @ID_HOADON NVARCHAR(10), @ID_KH NVARCHAR(10), @HINH_THUC INT, @ID_SHIPPER NVARCHAR(10) = NULL,
    @CHIETKHAU MONEY = 0, @GioHang T_GioHang READONLY
AS
BEGIN
    SET NOCOUNT ON; BEGIN TRANSACTION;
    BEGIN TRY
        DECLARE @TONG_TIEN_SP MONEY, @TONG_THANH_TOAN MONEY;
        SELECT @TONG_TIEN_SP = SUM(SO_LUONGBAN * DON_GIA) FROM @GioHang;
        SET @TONG_THANH_TOAN = @TONG_TIEN_SP - @CHIETKHAU;

        INSERT INTO HOADON (ID_HOADON, ID_KH, NGAY_TAO, CHIETKHAU, TONG_TIEN_SP, TONG_THANH_TOAN, HINH_THUC, TRANG_THAI, ID_SHIPPER)
        VALUES (@ID_HOADON, @ID_KH, GETDATE(), @CHIETKHAU, @TONG_TIEN_SP, @TONG_THANH_TOAN, @HINH_THUC, 1, @ID_SHIPPER);

        INSERT INTO DON_HANG (ID_HOADON, ID_SANPHAM, SO_LUONGBAN, DON_GIA)
        SELECT @ID_HOADON, ID_SANPHAM, SO_LUONGBAN, DON_GIA FROM @GioHang;

        UPDATE KHO_HANG
        SET SO_LUONG = KHO_HANG.SO_LUONG - gh.SO_LUONGBAN
        FROM KHO_HANG JOIN @GioHang gh ON KHO_HANG.ID_SANPHAM = gh.ID_SANPHAM;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION; THROW;
    END CATCH
END;
GO

-- 6. SP Thêm Khách Hàng
CREATE PROCEDURE SP_ThemKhachHang
    @ID_KH NVARCHAR(10), @TEN_KH NVARCHAR(100), @SO_DTH VARCHAR(20) = NULL,
    @DIA_CHI NVARCHAR(250) = NULL, @THANH_PHO NVARCHAR(50) = NULL, @QUOC_GIA NVARCHAR(50) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    IF EXISTS (SELECT 1 FROM KHACHHANG WHERE ID_KH = @ID_KH) BEGIN RAISERROR (N'Mã khách hàng này đã tồn tại.', 16, 1); RETURN; END
    INSERT INTO KHACHHANG (ID_KH, TEN_KH, SO_DTH, DIA_CHI, THANH_PHO, QUOC_GIA, DIEM_TICH_LUY, TRANG_THAI)
    VALUES (@ID_KH, @TEN_KH, @SO_DTH, @DIA_CHI, @THANH_PHO, @QUOC_GIA, 0, 1);
END;
GO

-- 7. SP Sửa Khách Hàng
